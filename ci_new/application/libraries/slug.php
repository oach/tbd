	/**
	 * Format a string for being used for a slug, image, or other "safe" path/filename
	 * @param  string $string the value that needs to appropriately formatted
	 * @param  string $separator the string that should separate each piece of the string that is returned
	 * @return string formatted string
	 */
	public function url_format($string, $separator = '-')
	{
		$string = strtolower(trim($string));
		$string = html_entity_decode($string); 
		$string = $this->ci->slug->_replace_accents($string);
		
		$find = array(' ', '&', '\r\n', '\n', '+', ',', '/');
		$string = str_replace($find, '-', $string);
	 	
	 	$find = array('/[^a-z0-9\-<>]/', '/[\-]+/', '/<[^>]*>/');		
		$repl = array('', $separator, '');
		$string = preg_replace($find, $repl, $string);
		
		return $string;
	}
}
