				        <div class="green bold panel-heading">Morey Equation</div>
				        <ul class="list-group">
							<li class="list-group-item">This equation is typically used in beer software to approximate color.</li>
							<li class="list-group-item">SRM = 1.4922 * (MCU ^ 0.68590)</li>
							<li class="list-group-item">good for color < 50 SRM</li>
							<li class="list-group-item">MCU = grainColor * grainWeightLBS / volumeGallons</li>
						</ul>
