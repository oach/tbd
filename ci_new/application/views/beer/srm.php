					<div class="panel panel-default">
				        <div class="green bold panel-heading">degress Lovibond</div>
				        <ul class="list-group">
							<li class="list-group-item">Was the first scale to officially and uniformily measure the the color of beer in the 1860s.</li>
							<li class="list-group-item">A sample of the beer was placed in a &#34;Tintometer&#34; and compared with several glass slides.</li>
							<li class="list-group-item">The color disc that most closely represented the beer was than assigned to the beer.</li>
						</ul>
