				<h2 class="srm brown">Standard Reference Method (SRM)</h2>
				<p>We, as humans, are very visual.  We use this sense to give us quick feedback on many aspects of life.
				Brewers only have one chance to set the first impression of a beer as it pours into our glass.  Based on
				the style of the beer, the color is the first gauge for what we expect in a beer.  Other factors like
				retention, lacing, and color of the foam play a big part also.</p>
				<p>SRM is a scale for measuring the color intensity of a beer (it&#39;s more technical than this, but
				we will leave it there).  SRM was adopted by the Ameican Society of Brewing Chemists in 1951.  There is
				another, older scale named degrees Lovibond (devised by Joseph Williams Lovibond) that the SRM scale
				has replaced.</p>
				<p>The color chart and colors below are an approximation of the actual colors in the SRM scale.  Two Beer Dudes
				tries to use these color descriptions or SRM number when rating beer.  We hope that you will do the same
				so that everyone is on a level playing field while describing our beer experience.  Enjoy!</p>
				
